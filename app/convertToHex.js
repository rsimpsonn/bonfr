export default function convertToHex(letter) {
  if (letter === 'a') return '#02A8F3';
  else if (letter === 'b') return '#FFC2FB';
  else if (letter === 'c') return '#89FFE4';
  else if (letter === 'd') return '#F8E81C';
  else if (letter === 'e') return '#6D71D7';
  else if (letter === 'f') return '#FF6969';
  else if (letter === 'g') return '#D02118';
  else if (letter === 'h') return '#F6A023';
  else if (letter === 'i') return '#3894FF';
  else if (letter === 'j') return '#A200FF';
  else if (letter === 'k') return '#FF0084';
  else if (letter === 'l') return '#85FDA6';
  else if (letter === 'm') return '#B8E986';
  else if (letter === 'n') return '#F42E7E';
  else if (letter === 'o') return '#50E3C2';
  else if (letter === 'p') return '#3981AD';
  else if (letter === 'q') return '#CE374A';
  else if (letter === 'r') return '#5221FF';
  else if (letter === 's') return '#B3A3E9';
  else if (letter === 't') return '#E3FFC2';
  else if (letter === 'u') return '#99F5F4';
  else if (letter === 'v') return '#FF7300';
  else if (letter === 'w') return '#0A9C7C';
  else if (letter === 'x') return '#30D723';
  else if (letter === 'y') return '#FFE800';
  return '#4990E2';
}
